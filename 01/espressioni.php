<?php
$x =4;
$y = 2;

// Addizione
$addizione = $x + $y;
echo $addizione . "\n";

// Sottrazione
$sottrazione = $x - $y;
echo $sottrazione . "\n";

// Moltiplicazione
$moltiplicazione = $x * $y;
echo $moltiplicazione . "\n";

// Divisione
$divisione = $x / $y;
echo $divisione . "\n";

// Potenza
$potenza = $x ** $y;
echo $potenza . "\n";

// Modulo
$modulo = $y % $x;
echo $modulo . "\n";